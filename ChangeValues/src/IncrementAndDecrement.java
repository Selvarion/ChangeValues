public class IncrementAndDecrement {
    public static void main(String[] args) {

        int x = 1;
        int y = 2;

        while(x<=1){
            x++;

        }
        while(y>=2){
            y--;

        }

        System.out.println("Incremented x: " + x);
        System.out.println("Decremented y: " + y);
    }
}
